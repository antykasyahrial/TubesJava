    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        a1.setNoRekening(NoRekeningTxt.getText());
        a1.setNamaRekening(NamaRekeningTxt.getText());
        a1.setPassword(String.valueOf(PasswordTxt.getPassword()));
        
        String query = "SELECT * FROM `akun` WHERE `no_rek` = ?";
        
        try {
            ps = Database.getConnection().prepareStatement(query);
            
            ps.setString(1, a1.getNoRekening());
            rs = ps.executeQuery();
               
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "This Account is Already Exist");
            } else {
                a1.saveData();
                JOptionPane.showMessageDialog(null, "Your Account Has Been Created");
                NoRekeningTxt.setText("");
                NamaRekeningTxt.setText("");
                PasswordTxt.setText("password");
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_AddButtonActionPerformed

