    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        a1.setNoRekening(NoRekeningTxt.getText());
        a1.setNamaRekening(NamaRekeningTxt.getText());
        a1.setPassword(String.valueOf(PasswordTxt.getPassword()));
        
        String query = "SELECT * FROM `akun` WHERE `no_rek` = ? AND `nama_rek` = ? AND password_rek = ?";
        
        try {
            ps = Database.getConnection().prepareStatement(query);
            
            ps.setString(1, a1.getNoRekening());
            ps.setString(2, a1.getNamaRekening());
            ps.setString(3, a1.getPassword());
            rs = ps.executeQuery();
            
            int opsi = JOptionPane.showConfirmDialog(null, "Are You Sure to Delete Your Accont ?", "Confirmarion", JOptionPane.OK_CANCEL_OPTION,
            JOptionPane.QUESTION_MESSAGE);
                
            if(opsi == JOptionPane.YES_OPTION){
                rs.next();
                JOptionPane.showMessageDialog(null, "Your Account Has Been Deleted");
                
                NoRekeningTxt.setText("");
                NamaRekeningTxt.setText("");
                PasswordTxt.setText("password");
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
        }        
    }//GEN-LAST:event_DeleteButtonActionPerformed

